                               <p> The upholstery fabric for a new sofa or armchair is a big investment—you
                                    don’t want to be second-guessing it a few months after the piece arrives
                                     on your doorstep. Choose the wrong material and you’ll find yourself
                                      battling snags and stains. Make a mistake on the color or pattern,
                                       and the whole room suffers (or you’ll wind up splurging to have the
                                        piece reupholstered). So before you pick your new upholstery fabric
                                        , read on to find out what you should consider in terms of durability
                                        , comfort, and style. Here's how to zero in on a fabric choice you’ll 
                                        be happy to live with for years.</p>

                                
                               <p> <h3>What Is Upholstery, Anyway?</h3>
                                Upholstery is the materials—which include fabric, padding, 
                                webbing, and springs—that make up the soft coverings of chairs,
                                 sofas, and other furniture. The process began in the Middle Ages
                                  and grew in popularity during the 17th, 18th, and 19th centuries
                                  . Over the years, a wide variety of materials have been used, from
                                   hay to horsehair to wool. Modern upholstered items use metal sprin
                                   gs and foam for greater durability. While the interior materials ma
                                   y not be visible, they make a big difference in whether the piece is
                                    comfortable . And though fabric can always be changed—and may need t
                                    o be, depending on wear and tear—choosing high-quality inner layers 
                                    will help make sure the piece will last for years to come.</p>
                            

                                
                        

                                <p> <h3>How to Choose the Best Upholstery Fabric for Your Sofa (or Chair, or Headboard…)</h3>
                                    As you shop for an upholstery fabric, let your practical needs lead, advise Mitchell 
                                    Gold and Bob Williams, cofounders of the home-furnishings company that shares their 
                                    names. “First, consider how you live and who will use the piece,” says Bob. “This will
                                     help guide you on the type of material to choose.” You may love the look of that silk 
                                     velvet, but it will quickly get destroyed in a house with kids or pets. Pieces in high-
                                     traffic areas, such as family or living rooms, will need durable fabrics, while furnitur
                                     e that doesn't get as much wear and tear, such as a bedroom settee or headboard, can spo
                                     rt any type of textile.</p>

                                <p>A big mistake people make, Bob says, is not considering how a fabric or leather may ag
                                    e over time. Check the label and ask questions at the showroom to find out about the m
                                    aterial’s content and cleaning needs, then think about how much maintenance you’re prepa
                                    red to do to protect your selection. “How will it look in five years?” asks Bob. “Will 
                                    you still love the leather as it develops a nice patina from everyday living? Will you v
                                    acuum the upholstery fabric regularly? Will you close the drapes when away or not using a 
                                    room to avoid some of the fading that naturally occurs from exposure to sunlight?”</p>



                                    <p> <h3>The Best Upholstery Fabrics for Homes with Kids and Pets</h3>
                                        “Growing up, my brother and I weren’t allowed in the living room; it was 
                                        for company only,” says Mitchell. Today, though, there are plenty of upholstery
                                         materials that match good looks with durability. “For children and pets—and maybe
                                          red-wine lovers—we like the easy care of a faux suede or the durability of a
                                           Sunbrella indoor-outdoor fabric,” he says. “We’re also big fans of slipcovered
                                            upholstery. And distressed leather is great—you don’t have to worry about the 
                                            occasional scuff or scratch.” If you have a busy household, stay away from delicate
                                             or textured options, like silk, which could pull and aren’t as forgiving when it 
                                             comes to stains.</p>

                                             <p> <h3>Think Outside the Swatch</h3>
                                                You're not limited to the fabrics in the store, especially if you have 
                                                a great upholsterer in your Rolodex (or some serious DIY skills). Consider
                                                 unconventional materials such as vintage blankets or kilim rugs. Bonus points 
                                                 if you pair a bold choice of fabric on a traditional piece such as a wingback 
                                                 chair or camelback sofa.</p>
                                    
                               


